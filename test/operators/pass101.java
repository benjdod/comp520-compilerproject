class Test {

    /**
     * Short assignment operators
     */

    public static void main(String[] args) {
        
        int x = 56;
        x -= x;                     // 0
        x += 2;                     // 2
        x *= 20;                    // 40
        x /= 4;                     // 10
        x %= 3;                     // 1

        System.out.println(x);      // >>> 1
    }
    
}
